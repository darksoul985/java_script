/**
 * Функция сложения
 * @param   {number} num1 - первое число
 * @param   {number} num2 - второе число
 * @returns {number}
 */
function addition(num1, num2) {
    return num1 + num2;
}

/**
 * Функция деления
 * @param   {number} num1 - первое число
 * @param   {number} num2 - второе число
 * @returns {number}
 */
function difference(num1, num2) {
    return num1 - num2;
}

/**
 * Функция умножения
 * @param   {number} num1 - первое число
 * @param   {number} num2 - второе число
 * @returns {number}
 */
function multiplication(num1, num2) {
    return num1 * num2;
}

/**
 * Функция деления
 * @param   {number} num1 - первое число
 * @param   {number} num2 - второе число
 * @returns {number}
 */
function division(num1, num2) {
    return num1 / num2
}
